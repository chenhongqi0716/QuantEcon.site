<h1>About</h1>

<p>QuantEcon is a community based organization run by economists 
and backed by the <a href="http://www.sloan.org/">Alfred P. Sloan Foundation</a>.
Its aim is to coordinate development of high quality open source code for
quantitative economic modeling.</p>

